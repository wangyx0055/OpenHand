			<a href="#panel3">Open Hand philosophy about the items we give</a>
			<div id="panel3" class="content">
				<b>Open Hand never wants a recipient to perceive that we care so little for them that they are getting second-rate food or materials</b>.
				Every item that we give must be checked for expiration dates. 
				To the maximum extent, Open Hand will give visually appealing food (not old, bent, damaged cans, etc).</br>
				</br>
				Occasionally Open Hand receives items to distribute that are used and worn. 
				Exercise good judgment when passing out those items. Only new(er), clean items should generally be given. 
				Does it give the impression that we are trying to give “hand-me-downs” to them? 
				If so, then that item shouldn’t be passed along. 
				Many of these people have already had these items carelessly thrown their direction. 
				We won’t do that. 
				They can be treated like that anywhere else. 
				Not Open Hand.</br>
				</br>
				Of course, nothing is in concrete. Ask the Lord if you’re not sure. How the receiver would feel receiving this item?</br>
				</br>
				<b>Open Hand NEVER gives expired, damaged or opened food</b>. On occasion, we’ll give big containers of opened items (coffee, etc), but the recipient must be clearly told it that has been opened. 
				It must be received as a choice (i.e. off the miscellaneous table) - not placed in their box as a staple.  
				There are times where the food is clearly good, within expiration, and would benefit someone. 
				That is okay to pass out if they are told about its condition. 
				These situations are the exception and not the rule.</br>
